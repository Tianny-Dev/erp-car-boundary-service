<div class="article-title">
  Article 4 (Operational Management)
</div>

<p>
    <strong>4.1. Driver Assignment and Operational Planning:</strong> __________ shall recruit
    and assign taxi drivers to operate leased vehicles, and establishes daily operational
    plans to manage efficient vehicle operation. Each driver operates according to the
    dispatching schedule and routes established by both DDGNS and __________. And
    __________ shall effectively manage and supervise assigned drivers to ensure
    compliance with safety regulations and quality service standards.
</p>

<p>
    <strong>4.2. Training and Administrative Guidance:</strong> __________ provides sufficient
    training to assigned taxi drivers before commencing operation and provides guidance
    on service quality, safe operation, and the use of digital payment systems. Furthermore,
    __________ shall provide ongoing administrative guidance and support to drivers
    throughout the operation period to ensure compliance with relevant laws and
    regulations, and also the terms of this AGREEMENT. Drivers must comply with
    __________'s internal regulations and instructions, and __________ may take
    necessary corrective or disciplinary action in the event of violations or management
    and operational issues.
</p>

<p>
    <strong>4.3. Operational Status Reporting:</strong> __________ monitors and manages daily
    vehicle operation status and monitors daily status reports through the digital system
    provided by DDGNS. The system records each vehicle's mileage, remaining battery
    capacity, charging or battery replacement history, driving time, and settlement results
    in real time. __________ monitors this data and, if any abnormalities (e.g., signs ofvehicle breakdown, excessive speeding, etc.) are detected, immediately notifies the driver and takes necessary action.
</p>

<p>
    <strong>4.4. Reporting and Consultation:</strong> DDGNS analyzes overall operational
    performance based on the operation and revenue data collected through the system
    and regularly shares this with __________. Through monthly operation reports and
    meetings, the PARTIES discuss operational performance, revenue distribution status,
    and if there are management issues or concerns, the PARTIES shall negotiate
    corrections and/or improvements.
</p>

<p>
    <strong>4.5. Independence of Relationship:</strong> __________ operates the leased vehicles
    as an independent operating entity under this AGREEMENT. No employment or
    contractual relationship need not exists between DDGNS and the taxi drivers. However,
    drivers perform their duties solely under the direction and supervision of __________,
    and there is no employment or direct legal relationship between DDGNS and the
    drivers. __________ informs the drivers of this and assumes primary responsibility for
    any issues that may arise during operation. However, if DDGNS's cooperation is
    required on matters related to the operation of the digital payment system or vehicle
    technology, DDGNS will provide support within a reasonable scope based upon
    __________'s request.
</p>

<p>
    <strong>4.6. Compliance with Laws and Maintaining Licenses:</strong> __________ shall
    operate its taxi transportation business in compliance with Philippine traffic laws and
    related driving regulations. In particular, it shall always maintain the taxi operating
    license (franchise) and vehicle registration requirements required by local traffic
    regulatory agencies (e.g., the LTFRB) and faithfully fulfill license renewal and reporting
    obligations. If __________'s license is suspended or revoked due to its own fault, this
    shall constitute a material breach of this AGREEMENT.
</p>