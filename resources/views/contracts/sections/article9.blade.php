<div class="article-title">
  Article 9 (Taxes and Legal Compliance)
</div>

<p>
    <strong>9.1. Tax Payment Responsibilities:</strong> Each Party shall be responsible for all
    taxes, fees, and charges arising in connection with the performance of this
    AGREEMENT, in accordance with applicable laws. Specifically, DDGNS shall report
    and pay taxes required by Philippine tax law related to the receipt of lease fees (daily
    boundary fees) and the payment of such fees. __________ shall also faithfully pay
    applicable taxes on the operating fees received under this AGREEMENT. Both
    PARTIES shall identify and comply with their obligations under relevant tax laws,
    including Value Added Tax (VAT) and Withholding Tax. Where necessary, both
    PARTIES shall cooperate and exchange necessary tax reporting documents and
    provide tax reporting records or receipts upon request to the other party within a
    reasonable scope.
</p>

<p>
    <strong>9.2. Compliance with Laws and Regulations:</strong> The PARTIES shall perform this
    AGREEMENT in strict compliance with the laws and applicable government
    regulations of the Republic of the Philippines. In particular, they shall comply with all
    laws applicable to this Project, including traffic laws, labor laws (regarding driver
    treatment), and environmental laws (regarding the operation of electric vehicles).
    DDGNS shall comply with laws and regulations regarding vehicle safety standards,
    import customs clearance, and registration. __________ shall comply with laws
    governing passenger vehicle transportation businesses (e.g., LTFRB regulations) and
    shall adhere to licensing requirements and regulations governing operating areas and
    operating hours.
</p>

<p>
    <strong>9.3. Adjustments due to changes in tax laws/regulations:</strong> If, during the term
    of this AGREEMENT, the PARTIES significantly impact the PARTIES' cost burden or
    revenue structure due to amendments to Philippine tax laws, the introduction of new
    tax items, or changes in transportation/cooperative regulations, the PARTIES may
    equitably adjust the terms of this AGREEMENT (particularly the fare and revenue
    sharing structure) through mutual agreement. For example, if the net receipts for the
    daily boundary fare decrease due to the imposition of a new tax, the PARTIES mayshare the impact or renegotiate the fare level. Any adjustments must be incorporated into this AGREEMENT by another written Agreement.
</p>

<p>
    <strong>9.4. Legal Disputes and Responsibility:</strong> If a legal claim or dispute arises from
    a Third Party during the performance of this AGREEMENT (e.g., a claim from a
    passenger or driver, a corrective order from a government agency, etc.), the PARTIES
    involved will immediately notify the other Party and cooperate in responding. Unless
    the dispute is solely due to the fault of either Party, the PARTIES will jointly endeavor
    to resolve the issue and, if necessary, share the costs and responsibilities of legal
    action in a reasonable ratio.
</p>

<p>
    <strong>9.5. Compliance Monitoring and Reporting:</strong> If local administrative agencies or
    regulatory bodies request monitoring or reporting regarding the operation of this project,
    __________ will notify DDGNS and provide the necessary information. Similarly,
    DDGNS may request __________'s operational data or compliance status for reasons
    such as project financing or investor reporting, and __________ will cooperate within
    a reasonable scope. The two PARTIES will regularly consult to ensure business
    transparency and compliance and, if necessary, may initiate internal or external audits.
</p>