<div class="article-title">
  Article 6 (Accidents and Insurance Handling)
</div>

<p>
    <strong>6.1. Insurance:</strong> DDGNS subscribes to comprehensive vehicle insurance to
    protect against accidents and damages that may arise in connection with leased
    vehicles and their operation. The insurance policy is issued in DDGNS's name and
    includes standard coverage for bodily injury, property damage, and vehicle damage. If
    necessary, the driver or __________ may be designated as an additional insured
    person, or additional insurance provisions may be added to cover special risks inherent
    in __________ operations.
</p>

<p>
    <strong>6.2. Initial Response in the Event of an Accident:</strong> In the event of a traffic
    accident or vehicle damage while operating a leased vehicle, __________ assumes
    primary responsibility for responding. Specifically, the driver of the vehicle must
    immediately ensure the safety of passengers, take emergency measures such asrescue operations if necessary, and report the accident to the relevant authorities,
    including the Philippine Traffic Police. Upon receiving a report of the accident,
    __________ will immediately notify DDGNS of the accident and instruct the driver to
    collect necessary documentation, including an accident report, photographs, and
    witness statements.
</p>

<p>
    <strong>6.3. Insurance Claim Processing:</strong> In the event of an accident, __________
    proactively handles the insurance claim process in accordance with DDGNS's
    insurance policy. __________ promptly prepares and submits all accident-related
    documents and evidence to the insurance company and diligently supports any
    additional information or investigations required during the insurance process. DDGNS
    provides necessary assistance to __________ in smoothly processing the claim. Upon
    payment of insurance benefits, the benefits will be used primarily for vehicle repair
    costs, as agreed upon between the PARTIES. If any damages are not covered by the
    insurance benefits, separate negotiations will be held to determine whether
    __________ or the driver will be responsible for the accident, depending on who was
    at fault.
</p>

<p>
    <strong>6.4. Suspension of Operation and Provision of Replacement Vehicle:</strong> The
    vehicle involved in an accident will be immediately suspended from operation until
    safety and repairs are completed. __________ will minimize service interruptions by
    placing the driver on standby or assigning a replacement vehicle. If repairs to the
    damaged vehicle are expected to take an extended period of time, DDGNS will
    temporarily provide a replacement vehicle, if possible, to ensure __________'s
    continued service. Even if a replacement vehicle is provided, the boundary fee and
    revenue sharing structure stipulated in the original contract will remain in effect. If a
    replacement vehicle is unavailable and the vehicle remains out of service for a certain
    period of time, the PARTIES will negotiate to waive or adjust the daily boundary fee for
    the vehicle during that period.
</p>

<p>
    <strong>6.5. Accident Reporting and Recurrence Prevention:</strong> __________ will report
    all vehicular accidents in writing to DDGNS, detailing the circumstances and the
    consequences. In the event of a serious accident, the PARTIES will jointly analyze the
    cause and develop preventative measures, such as driver retraining and improving
    operating policies. If necessary, they will also discuss additional insurance coverage
    or the introduction of safety measures, and cooperate to improve overall operating
    safety.
</p>