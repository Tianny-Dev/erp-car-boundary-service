  Article 3 (Fare Collection and Revenue Distribution Structure)
</div>

<p>
    <strong>3.1. Fare Collection:</strong> All passenger fares generated from the operation of
    leased vehicles will be collected in real time into a central account under DDGNS's
    name through a QR-based digital payment system provided by DDGNS. All
    passengers are encouraged to use digital payment whenever possible. If payment in
    cash is unavoidable,
</p>

<p>
    <strong>3.2. Daily Boundary Fee Deduction:</strong> The daily boundary fee refers to the fixed
    daily rental fee per vehicle under this lease AGREEMENT, which is PHP 1,600 per
    vehicle. DDGNS will deduct the daily boundary fee (PHP 1,600) per vehicle from the
    total fare revenue for each business day as a priority. This amount is the amount
    DDGNS secures as consideration for the vehicle lease. Even if the vehicle's
    transportation revenue on a given day falls below PHP 1,600, __________ isresponsible for paying the full daily boundary fee to DDGNS, and any shortfall will be
    settled separately or covered by other revenue.
</p>

<p>
    <strong>3.3. Revenue Distribution:</strong> After deducting the daily boundary fee, DDGNS
    will first calculate the net revenue for the day, deducting the amounts calculated in
    accordance with Paragraphs 1 and 2 of this Article. DDGNS will allocate and pay the
    __________ operating fees out of this net revenue and distribute the remaining
    balance to the driver/operator of the vehicle. The specific percentage or amount of the
    operating fee will be separately agreed upon by and between the PARTIES (e.g., ○○%
    of net revenue) and will be specified in the attached AGREEMENT. Upon execution of
    this AGREEMENT. DDGNS will pay the agreed fee to __________ through bank
    transfer or other means from the net revenue calculated via the digital payment system
    after the end of each business day, and will transfer the remaining balance to the
    driver's e-wallet account or to any designated and preferred payment method.
</p>

<p>
    <strong>3.4. Settlement and Reporting:</strong> Settlement regarding revenue collection and
    distribution is conducted on a daily basis. DDGNS automatically calculates settlement
    details, including total revenue for each vehicle, deducted boundary fees, __________
    shares and commissions, and driver payments, at the end of each day through a
    mutually agreed digital payment system. These settlement results are transparently
    shared through an administrator portal accessible to both DDGNS and __________.
    __________ can also use this data to verify payment details for drivers. If necessary,
    the PARTIES will mutually verify the settlement details and, if any disagreement arise,
    negotiate and resolve them amicably.
</p>