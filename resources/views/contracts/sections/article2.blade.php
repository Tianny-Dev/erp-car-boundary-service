<div class="article-title">
  Article 2 (Lease Structure)
</div>

<p>
  <strong>2.1. Vehicle Ownership and Lease:</strong> DDGNS will own the electric taxi vehicles 
  procured through the project financing and lease the same vehicles (collectively, the 
  "Leased Vehicles") to <span class="line">{{ $franchise_name }}</span> in accordance with the roles and responsibilities 
  set forth in this Agreement. It is immediately understood that DDGNS will retain legal 
  ownership of the deployed electric vehicles during the term of this Agreement.
</p>

<p>
    The detailed lease terms are as follows:
</p>

<ul>
  <li>Number of Leased Vehicles: {{ $vehicle_count }}</li>
  <li>Security Deposit: No initial down payment is required, and the security deposit shall be equivalent to {{ $security_days }} days of lease payment.</li>
  <li>Daily Boundary Fee: {{ number_format($daily_boundary) }} pesos</li>
</ul>

<p>
  <strong>2.2 Operation and Sublease:</strong> __________ is responsible for the operation, 
  management, and maintenance of the Leased Vehicles entrusted to its care for 
  operations in Baguio City and nearby municipalities of Benguet Province. Furthermore, 
  __________ may entrust operation of the vehicles to its own drivers or taxi drivers who 
  are members of the Cooperative. And if applicable or necessary, __________ may 
  sub-lease each Leased Vehicle to individual taxi operators/drivers. In such cases, 
  __________ will ensure that these drivers comply with the terms of this AGREEMENT, 
  and any ensuing or subsequent agreements with these drivers must be aligned with 
  and consistent with this AGREEMENT.
</p>
