    Notices are deemed officially received upon delivery if delivered personally, upon
    seven days' delivery if sent by registered mail, upon confirmation of receipt if sent
    by courier, or upon the recipient's acknowledgement of receipt if sent by email.
</p>

<p>
    <strong>10.7. Governing Law and Dispute Resolution:</strong> This AGREEMENT shall be
    construed and enforced in accordance with the laws of the Republic of the Philippines.
    Should any dispute or disagreement arise in connection with this AGREEMENT, the
    PARTIES shall first endeavor to resolve it through amicable negotiation. Any dispute
    not resolved through negotiation shall be subject to the exclusive jurisdiction of the
    courts of competent jurisdiction located in Baguio City, Philippines, and shall be finally
    resolved therein. However, the PARTIES may, by written AGREEMENT, elect another
    dispute resolution method, such as international arbitration, in which case the agreed-
    upon procedures shall apply.
</p>

<p>
    <strong>10.8. Language of the AGREEMENT:</strong> This AGREEMENT may be drawn up in
    Korean and English, and both versions shall have equal effect. In the event of any
    conflict or inconsistency in interpretation between the two versions, the English version
    shall prevail. (The Korean version shall be considered a reference translation between
    the PARTIES, and signing and notarization may be based on the English version.)
</p>

<p>
    <strong>10.9. Future Cooperation:</strong> The PARTIES agree to cooperate in good faith with
    each other if additional documents or actions are required to achieve the purposes of
    this AGREEMENT. In particular, the PARTIES agree to sign additional contracts,
    confirmations, memoranda, etc. or provide information within a reasonable scope
    requested by financial institutions or government agencies. Furthermore, the PARTIES
    will actively communicate with each other to successfully promote the electric taxi
    business, and if revisions to the AGREEMENT are necessary to respond to changing
    market conditions or technological advancements, they will pursue them through
    consultation.
</p>

<p>
    <strong>10.10. Reservation of Rights:</strong> All rights and powers not expressly granted in
    this AGREEMENT are reserved by DDGNS and __________, respectively. No delay
    or limitation by either Party in exercising any right it has against the other Party shall
    be construed as a waiver of that right or any other right.
</p>