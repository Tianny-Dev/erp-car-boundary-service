<div class="article-title">
  Article 10 (Other Provisions)
</div>

<p>
    <strong>10.1. Entire AGREEMENT:</strong> This AGREEMENT contains the entire
    AGREEMENT between the PARTIES regarding this transaction. All prior oral or written
    discussions, memoranda of understanding (MOUs), and draft Agreements between
    the PARTIES are superseded and rendered null and void. The PARTIES acknowledge
    that they have not relied on any representations or warranties made by the other party
    prior to the execution of this AGREEMENT, unless expressly incorporated into this
    AGREEMENT.
</p>

<p>
    <strong>10.2. Amendment to this AGREEMENT:</strong> Any amendment or modification to this
    AGREEMENT shall be effective only upon the written AGREEMENT of both PARTIES.
    No waiver by either party shall be effective unless expressly provided in writing, and
    no delay or failure by either party to exercise any provision shall constitute a waiver of
    that provision.
</p>

<p>
    <strong>10.3. Severability:</strong> If any provision of this AGREEMENT is held invalid or
    unenforceable by a local court or other competent authority, the remaining provisions
    shall remain in full force and effect. Any provision held invalid or unenforceable will be
    deemed replaced by a provision reasonably modified to reflect the PARTIES' original
    intent to the fullest extent permitted by law, and the PARTIES will cooperate to this
    end.
</p>

<p>
    <strong>10.4. Non-Assignment:</strong> Neither Party may assign or transfer any or all of its
    rights or obligations under this AGREEMENT to a Third Party without the prior written
    consent of the other Party. However, DDGNS may assign or establish a beneficial
    interest or security interest under this AGREEMENT to a financial institution for the
    purpose of performing a financial contract pursuant to project financing, and in such
    case, it will notify __________ in advance. Furthermore, __________ may perform
    some of its obligations under this AGREEMENT (such as vehicle operation obligations)through a sublease AGREEMENT with the driver, but this will not relieve DDGNS of its obligations under this AGREEMENT.
</p>

<p>
    <strong>10.5. Independent Contractors:</strong> The PARTIES are independent contractors in
    performing this AGREEMENT and will not be deemed to act on behalf of the other
    Party or as joint venturers. Nothing in this AGREEMENT shall be construed to
    constitute either Party as an agent, employee, or partner of the other Party.
    __________ is a separate legal entity independent from DDGNS, employing or
    contracting its own personnel (including drivers) to conduct operations. __________
    also assumes all legal liabilities related to its designated drivers, including taxes and
    social insurance.
</p>

