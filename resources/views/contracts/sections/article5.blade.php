<div class="article-title">
  Article 5 (Maintenance and Vehicle Management)
</div>

<p>
    <strong>5.1. Regular Inspections and Preventive Maintenance:</strong> DDGNS or an
    authorized maintenance center certified and/or affiliated with DDGNS is responsible
    for performing regular maintenance and preventive inspections of Leased Vehicles.
    DDGNS shall establish a maintenance schedule based on the vehicle manufacturer's
    recommended standards and notifies the __________ and unit driver/s in advance to
    have the vehicle delivered to the aforesaid maintenance center. DDGNS is responsible
    for covering maintenance costs (including replacement of routine consumables and
    preventive maintenance) unless otherwise agreed upon. __________ should
    cooperate with the maintenance schedule to ensure timely maintenance.
</p>

<p>
    <strong>5.2. Battery Swapping System:</strong> DDGNS establishes and operates a battery
    swapping infrastructure to ensure efficient operation of electric taxis. Whenever
    necessary, DDGNS shall provide prompt battery replacement services through its
    affiliated battery swap centers. Drivers must replace discharged batteries with charged
    ones only at designated exchange stations under the direction of the __________. Any
    battery usage or exchange fees incurred during this process will be determined and
    covered by a separate AGREEMENT between the PARTIES (if necessary, these fees
    can be deducted from the accumulated daily fare revenue).
</p>

<p>
    <strong>5.3. Daily Inspection and Maintenance:</strong> The __________ and unit drivers must
    cooperate in the daily maintenance and inspection of the vehicle. Before and after each
    trip, drivers must perform basic safety checks, including tire pressure, lighting, and
    brake status, and immediately report any abnormalities, if any, to the __________. The
    __________ records and compiles reports of vehicle abnormalities or breakdowns
    received from drivers, promptly relays them to DDGNS or a designated maintenance
    center, and coordinates necessary remedial actions. The __________ must also
    support daily maintenance tasks, such as maintaining vehicle cleanliness and
    providing driver training on vehicle handling.
</p>

<p>
    <strong>5.4. Breakdown and Repair:</strong> In the event of a vehicle breakdown or
    performance abnormality, the __________ will immediately suspend operation and
    notify DDGNS to request repairs. DDGNS will endeavor to repair the vehicle or provide
    replacement parts as quickly as possible to resume immediate operation. The vehicle
    will not be operated until repairs are completed, and __________ must take steps to
    ensure that the driver is assigned another leased vehicle during this breakdown period.
</p>

<p>
    <strong>5.5. Prohibition of Unauthorized Repairs and Modifications:</strong> __________
    and the driver may not make any modifications, alterations, or major repairs to the
    leased vehicle without DDGNS's approval. Minor repairs requiring emergency repairs
    (e.g., light bulb replacement) must be reported to DDGNS, even after the fact.
    __________ will bear the full cost of repairs for any malfunction or damage resulting
    from __________'s unauthorized modifications or abnormal handling of the vehicle,
    which may even constitute a breach of this AGREEMENT.
</p>

<p>
    <strong>5.6. Vehicle Replacement and Spare Vehicles:</strong>
    DDGNS strives to maintain a certain number of spare vehicles or spare parts to
    ensure smooth taxi operations. In the event of extended vehicle repairs, serious
    breakdowns, or other reasons that result in extended service interruption, DDGNS will
    endeavor to provide __________ with a replacement vehicle within a reasonable range,
    and __________ will accept the replacement vehicle to continue operations. If
    provision of a spare vehicle is not possible, the parties may negotiate a deferral or
    reduction of the daily boundary fee for the vehicle during the period of suspension,
    after agreeing on responsibility for the unscheduled service.
</p>