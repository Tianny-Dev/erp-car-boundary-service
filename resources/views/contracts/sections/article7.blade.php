<div class="article-title">
  Article 7 (Digital Payment and Settlement)
</div>

<p>
    <strong>7.1. QR-Based Digital Payment System:</strong> DDGNS will build and operate a QR
    code-based digital fare payment system for the operation of electric taxis in Baguio
    City. This system allows passengers to check the fare matrix from boarding to alighting,
    and scan the QR code presented upon disembarkation to make mobile payments (via
    e-wallets such as GCash and Maya, or credit cards/NFC payments to be introduced in
    the future). Upon completion of the digital payment, the amount is immediately
    deposited into DDGNS's central account, and a payment authorization signal is
    transmitted to the vehicle's digital meter and the settlement server.
</p>

<p>
    <strong>7.2. Real-Time Settlement Data Sharing:</strong> DDGNS's settlement server
    integrates and manages each vehicle's driving records, digital payment details, and
    cash receipt reports, and automatically generates a daily settlement ledger. This
    system includes an automatic deduction function for daily boundary fares and
    maintains an audit log of all transactions and settlement details. __________ can view
    each vehicle's real-time operation and income status, as well as settlement results,
    through the system's administrator portal or dedicated application. If necessary,
    __________ can submit objections or requests for review of settlement data through
    the system. DDGNS will review these objections and, if justified, revise the settlement
    data or provide the necessary explanations.
</p>

<p>
    <strong>7.3. Cash Receipt Processing:</strong> If a passenger must pay the taxi fare in cash,
    the driver must record the cash receipt in the system by pressing the "Cash Received"
    function on the vehicle's digital meter. This recorded cash receipt is temporarily stored
    in the settlement server as unpaid, and the driver must pay the amount directly to the
    __________-designated account or settlement office within the designated closing
    time after the end of business on that particular day. __________ aggregates and
    verifies the cash collected by all drivers within a specified period from the end of each
    day's operation (e.g., 10:00 PM or a closing time agreed upon with DDGNS) and
    deposits the amount into DDGNS's designated bank account. Once the cash payment
    amount is deposited and confirmed, the cash transaction is completed on the
    settlement server and a receipt is issued.
</p>

<p>
    <strong>7.4. Penalty for Late Payment:</strong> If __________ fails to deposit the cash payment
    into the DDGNS account within the specified time period as stipulated in the preceding
    paragraph, or if the driver delays cash payment without justifiable cause, DDGNS may
    impose late payment interest or late payment penalties on the amount due. Late
    payment interest is assessed at a daily rate of __% for each day of delay, or a fixed
    late payment penalty is imposed per transaction (the specific penalty rate or amount
    will be separately agreed upon). In this case, DDGNS will notify __________ of the
    late payment penalty and may deduct it from __________'s future commission
    payments or charge it separately.
</p>

<p>
    <strong>7.5. Payment System Maintenance:</strong> DDGNS is responsible for the smooth
    operation and security of the digital payment system and regularly inspects and
    updates components such as the payment gateway, settlement server, in-vehicle
    digital meter, and driver app. In the event of revenue collection disruptions due to
    system failures, payment network errors, or other reasons, DDGNS will promptly take
    recovery measures and, if necessary, manually correct transaction details to ensure
    accurate settlement. __________ will collect system errors and proposed
    improvements reported by drivers on site and forward them to DDGNS for system
    improvement/s.
</p>

<p>
    <strong>7.6. Data Access and Confidentiality:</strong> All operation and revenue data collected
    and stored through the digital payment and settlement system will be used for the
    performance of this AGREEMENT, and DDGNS and __________ may freely access
    such data. However, both PARTIES must obtain the other Party's prior consent before
    providing the collected data to a Third Party or using it for purposes other than thosestipulated under this AGREEMENT. Both PARTIES will make every effort to maintain the utmost confidentiality and security of the data.
</p>