<div class="article-title">
  Article 1 (Purpose)
</div>

<p>
  The purpose of this AGREEMENT is to clarify the rights and obligations of the
PARTIES by regulating all matters related to the vehicle leasing structure, fare
collection and profit-sharing, operational management, maintenance, and insurance
arrangements, regarding the lease and operations of electric taxi vehicles procured
and owned by DDGNS through project financing to <span class="line">{{ $franchise_owner_name }}</span>, and for
<span class="line">{{ $franchise_name }}</span> to operate and sustain taxi transportation services in Baguio City and
nearby municipalities of Benguet Province.
</p>
