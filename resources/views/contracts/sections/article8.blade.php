<div class="article-title">
  Article 8 (Contract Term and Early Termination)
</div>

<p>
    <strong>8.1. Contract Term:</strong> The basic contract term of this AGREEMENT shall be six
    years. The contract term shall commence on the effective date of this AGREEMENT
    (scheduled for __ month __ day, 2025) and expire on the sixth anniversary of the
    contract. The AGREEMENT may be renewed or extended by written AGREEMENT
    between the PARTIES three (3) months prior to its expiration. In the absence of a
    separate renewal AGREEMENT, this enforced AGREEMENT shall terminate on the
    expiration date.
</p>

<p>
  <strong>8.2. Grounds for Early Termination:</strong>
  Either Party may terminate this AGREEMENT early by written notice to the other
  Party in any of the following cases:
</p>

<p class="clause">
    <strong>8.2.1. Non-performance of contractual obligations (breach):</strong> If the other
    Party breaches a material obligation under this AGREEMENT and fails to remedy
    the breach within 30 days after receiving a written request for remedial action
    from the other Party. (e.g., __________'s persistent failure to pay the daily
    boundary fee, or DDGNS's failure to fulfill its vehicle maintenance obligations,
    etc.); and/or
</p>

<p class="clause">
    <strong>8.2.2. Loss of License or Bankruptcy:</strong> __________'s taxi transport business
    license (franchise) is revoked or prohibited by government authorities, or either
    Party enters into bankruptcy, dissolution, liquidation, or becomes insolvent.
</p>

<p>
    <strong>8.3. Illegal or Regulatory Changes:</strong> If the conduct of business under this
    AGREEMENT becomes illegal or significantly impracticable due to changes in
    Philippine laws or regulations. In this case, the PARTIES shall negotiate for sixty (60)
    days to adapt to the changed circumstances. If no alternative is found within that period,
    the AGREEMENT may be terminated early.
</p>

<p>
    <strong>8.4. Termination by Mutual Agreement:</strong> If both PARTIES agree to terminate
    the AGREEMENT early through a written AGREEMENT. The termination date and
    terms shall be determined by the PARTIES' AGREEMENT, and termination may be
    effected immediately without the procedures set forth in Paragraph 3. 3.
</p>

<p class="clause">
    <strong>8.4.1 Early Termination Procedure:</strong> If either Party wishes to terminate the
    Contract for any of the reasons set forth in Paragraph 2, the initiating Party must notify
    the other Party in writing of the reason/s that would effect Contract termination.
    However, termination by mutual AGREEMENT may be replaced by a written
    AGREEMENT signed by both PARTIES. The termination notice must include the
    reason for termination, the date of termination (effective date), and relevant supporting
    documentation (evidence of violations, if applicable). The other Party may raise
    objections or remedy the violation within the designated correction period after receipt
    of the notice (however, this does not apply in the case of mutually agreed termination).
</p>

<p class="clause">
    <strong>8.4.2. Compensation and Settlement:</strong> In the event of early termination, the
    PARTIES shall compensate for losses and effect the settlement in accordance with the
    following principles.
</p>

<p class="clause-letter">
    <strong>a. In the case of mutually agreed termination:</strong> The PARTIES shall negotiate
    the financial losses they will incur due to termination and their expected benefits
    for the remaining contract term, and determine the terms of compensation. For
    example, if __________ requests early termination due to circumstances
    attributable to __________, __________ may partially compensate for vehicle
    investment costs (such as the remaining daily boundary fee) that DDGNS cannot
    recover during the remaining period or pay a termination penalty. Conversely, if
    termination is due to circumstances attributable to DDGNS, DDGNS may
    compensate __________ for infrastructure or training costs invested in the
    contract. Such compensation agreements must be in writing before the
    termination takes effect.
</p>

<p class="clause-letter">
    <strong>b. In the case of termination due to a reason attributable to one Party,</strong> such
    as those set forth in Clause 1 or 2 of Paragraph 2, the Party at fault shall
    compensate the other Party for all direct damages incurred. In particular, if
    termination is due to __________'s fault, __________ shall immediately pay any
    unpaid boundary fees or other unsettled amounts and return the vehicle. If
    termination is due to DDGNS's fault, DDGNS will compensate __________ for
    its operating losses (e.g., loss of anticipated fee revenue for the remaining term)
    according to agreed-upon standards. The scope and method of compensation
    will be determined through negotiation between the PARTIES. If agreement is
    difficult, applicable Philippine laws and regulations will apply.
</p>

<p class="clause-letter">
    <strong>c. Termination due to changes in laws and regulations:</strong> In the event of
    termination pursuant to Paragraph 2, subparagraph 3, the termination is due to
    external factors rather than the fault of either Party. Therefore, the PARTIES
    agree to settle the dispute fairly, taking into account their respective investment
    costs and profit realization levels. Specifically, DDGNS will retrieve the provided
    vehicles and review any prepaid expenses (e.g., specific infrastructure
    installation costs) by __________ and reimburse them within a reasonable
    amount. __________ will settle and pay any outstanding boundary fees or other
    liabilities.
</p>

<p>
    <strong>8.5. Follow-up Actions Upon Termination:</strong> If this AGREEMENT expires or is
    terminated early, the following provisions must be implemented:
</p>

<p class="clause">
    <strong>8.5.1. Vehicle Return:</strong> __________ shall immediately return all Leased Vehicles
    in its possession to DDGNS (in compliance with the procedures stipulated in
    Article 2, Paragraph 4). Vehicles must be in good condition upon delivery, and
    DDGNS and __________ shall jointly inspect the vehicle condition and sign a
    handover document. If any repairs or maintenance are required, the costs will be
    settled based on responsibility.
</p>

<p class="clause">
    <strong>8.5.2. Final Settlement:</strong> Final settlement of all revenues and expenses incurred
    up to the end of the contract will be made in accordance with the principles ofArticles 3 and 7. DDGNS shall calculate the daily boundary fee up to the end of
    the contract and calculate and pay __________'s commission and driver
    payments. Any prepaid or deposited security deposits or reserves (if applicable)
    shall be returned or applied to the settlement in accordance with the terms of the
    contract.
</p>

<p class="clause">
    <strong>8.5.3. Return of Confidential Information and Assets:</strong> __________ shall
    promptly return or destroy any equipment (digital meters, communication devices,
    etc.), documents, and confidential information provided by DDGNS to DDGNS
    upon termination of this Contract. Access to digital payment system accounts
    may also be terminated or transferred by mutual AGREEMENT.
</p>

<p class="clause">
    <strong>8.5.4. Validity of Remaining Provisions:</strong> Even after termination of the
    AGREEMENT, provisions that by their nature should remain in effect, such as
    those related to damages, confidentiality, and dispute resolution, will remain in
    effect.
</p>